class Partido:
    def __init__(self, __nome, __ideologia, __sigla, __num):
        self.__nome = __nome
        self.__ideologia = __ideologia
        self.__sigla = __sigla
        self.__num = __num

    def getNome(self):
        self.__nome

    def getIdeologia(self):
        self.__ideologia

    def getSigla(self):
        self.__sigla

    def getNum(self):
        self.__num